package gitHub;

public class Day05_about_for3 {
	public static void main(String[] args) {
		
		
		for(int i = 0; i<5; i++) {
			for(int j=0; j<5; j++) {
				//System.out.printf("[%d , %d]",i+,j);
				System.out.print((i+j)+1 + " ");	//j에 더하는 것이 아니라 수전체에 1을 더해줘야함. 
			}
			System.out.println();
		}
		
		
	}//main
}
